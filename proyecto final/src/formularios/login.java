    private void btnentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnentrarActionPerformed
        String busqueda_usuario=m2.buscarusuarioRegistrado(txtusuario.getText(), txtcontra.getText());
        if(txtusuario.getText().equals("root")&& txtcontra.getText().equals("root")){
            JOptionPane.showMessageDialog(rootPane, "bienvenido iniciaste secion como administrador");
            menu m1=new menu();
            m1.text.setText("administrador");
             m1.setVisible(true);
            this.dispose();
        }else if(busqueda_usuario.equals("usuario encontrado")&& txtcontra.getText().equals("usuario encontrado")){
            String busqueda_nombre=m2.buscarusuario(txtusuario.getText());
            JOptionPane.showMessageDialog(rootPane, "Bienvenido "+busqueda_nombre);
             menu m1=new menu();
             m1.text.setText(busqueda_nombre);
             m1.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Usuario no registrado");
        }
        
    }//GEN-LAST:event_btnentrarActionPerformed

